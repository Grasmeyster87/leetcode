    let s0 = 0; // —Å—Ç–∞—Ä—Ç –ø–æ—Ç–æ—á–Ω–æ–≥–æ –∑—ñ–ª–ª—è –Ω–∞ —á–∞—Ä—ñ–≤–Ω–∏–∫—É 0

