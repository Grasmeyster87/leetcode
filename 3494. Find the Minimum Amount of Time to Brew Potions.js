        s0 = nextS0;
        prevPref = currPref; // —Ç–µ–ø–µ—Ä –ø–æ—Ç–æ—á–Ω–µ —Å—Ç–∞—î "–ø–æ–ø–µ—Ä–µ–¥–Ω—ñ–º" –¥–ª—è –Ω–∞—Å—Ç—É–ø–Ω–æ—ó —ñ—Ç–µ—Ä–∞—Ü—ñ—ó
