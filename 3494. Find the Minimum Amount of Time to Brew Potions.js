    const M = mana.length;

    // –ø—Ä–µ—Ñ—ñ–∫—Å–Ω—ñ —Å—É–º–∏ –¥–ª—è –ø–µ—Ä—à–æ–≥–æ –∑—ñ–ª–ª—è
